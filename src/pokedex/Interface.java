    private void btnConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmActionPerformed
        // TODO add your handling code here:
        
        lblDescription.setLineWrap(true);
        lblDescription.setWrapStyleWord(true); //Allows the description to 
                                               //be displayed across multiple
                                               //lines
        
        String direct = System.getProperty("user.dir"); //getting the project
                                                        //directory
        
        String[][] pokemon = Pokemon.getPokemon(); //defining the array
        
        int selection = pokeBox.getSelectedIndex(); //retrieving the selected
                                                    //pokemon's array number
        
        int imgnumber = selection + 1; //Pokemon's actual number (array index
                                       //+1) is used for the image name
        
        String imgstring = Integer.toString(imgnumber); //converting the int
                                                        //image number to a string
      
        String type1string = pokemon[selection][2];
        String type2string = pokemon[selection][3]; //retrieving the type(s) from  
                                                    //the array
        
        String imagedir = "/src/pokedex/Sprites/" + imgstring +".png";
        String type1dir = direct + "/src/pokedex/Types/" + type1string +".gif";
        String type2dir = direct + "/src/pokedex/Types/" + type2string +".gif";
        imagedir = direct + imagedir;  //forming strings for the directories
                                       //of the pokemon and type images
        
        image.setIcon(new ImageIcon(imagedir));
        lblHeight.setText(pokemon[selection][4]);
        lblWeight.setText(pokemon[selection][5]);
        lblDescription.setText(pokemon[selection][6]);
        lblType.setText(pokemon[selection][1]);
        lblType1.setIcon(new ImageIcon(type1dir));
        lblType2.setIcon(new ImageIcon(type2dir)); //Adjusting the text and 
                                                   //icon values according to 
                                                   //the user's selected Pokemon
        
        
        
        
    }//GEN-LAST:event_btnConfirmActionPerformed

